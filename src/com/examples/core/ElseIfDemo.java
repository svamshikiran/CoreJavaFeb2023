public class ElseIfDemo {

    public static void main(String args[]){

        int number = 45;

        if((number % 2) == 0){ // == != < > <= >= //Conditional Operators
            System.out.println("GIVEN NUMBER IS EVEN");
        }
        else{
            System.out.println("GIVEN NUMBER IS ODD");
        }

    }
}
